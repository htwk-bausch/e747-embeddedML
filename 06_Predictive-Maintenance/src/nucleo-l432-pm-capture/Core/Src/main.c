static void LIS3DH_Init(void) {

  uint8_t resp;
  uint8_t id;

  resp = LIS3DH_GetWHO_AM_I(&id);

  if (resp != MEMS_SUCCESS) {
    printf("Error communicating with LIS3DH\r\n");
    Error_Handler();
  }

  if (id != DEVICE_ID_LIS3DH) {
	printf("Unable to find LIS3DH IMU\r\n");
	Error_Handler();
  } else {
	printf("Found LIS3DH IMU sensor\r\n");
  }

  // setup LIS3DH sensor
  LIS3DH_SetODR(LIS3DH_ODR_1620Hz_LP);                                    // set output data rate to 1.62kHz
  LIS3DH_SetMode(LIS3DH_NORMAL);
  LIS3DH_SetFullScale(LIS3DH_FULLSCALE_2);	                              // set acc to -2...+2 G
  LIS3DH_SetAxis(LIS3DH_X_ENABLE | LIS3DH_Y_ENABLE | LIS3DH_Z_ENABLE);    // enable sensor
}

